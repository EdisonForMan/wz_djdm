<template>
  <div class="Map">
    <div :id="id" class="arcgisMap"></div>
    <transition name="fade">
      <djdmFrame ref="djdm" v-show="doFrame" />
    </transition>
    <img :src="tlimg" class="tlimg" />
  </div>
</template>

<script>
import { loadModules } from "esri-loader";
import { OPTION, spatialReference, IMAGELAYER } from "@/components/common/Tmap";
import {
  doPointLayer,
  doYqStreetLayer,
  doYqXSQLayer,
  doYqXqLayer,
  doYqFWLayer,
  fetchPoint,
  removeLayer
} from "./Arcgis.js";
import djdmFrame from "./components/djdmFrame.vue";

export default {
  name: "DjdmArcgis",
  data() {
    return {
      doFrame: false,
      tlimg: require("./img/tl.png")
    };
  },
  components: { djdmFrame },
  props: ["id"],
  created() {},
  async mounted() {
    await this.createMap();
    this.eventRegister();
    /** default xm */
    doYqStreetLayer(this);
    doYqXSQLayer(this);
    // doYqXqLayer(this);
    // doYqFWLayer(this);
    doPointLayer(this);
  },
  methods: {
    eventRegister() {
      //  顶部点击、左侧菜单点击,刷新点
      this.$hub.$on("document-checkbox", ({ check, id }) => {
        if (!id) return;
        const hash = {
          PointLayer: doPointLayer,
          xsqLayer: doYqXSQLayer,
          sqLayer: doYqXqLayer,
          streetLayer: doYqStreetLayer,
          fwLayer: doYqFWLayer
        };
        check && hash[id](this);
        !check && removeLayer(this, id);
      });
      this.$hub.$on("topDocumentClick", val => {
        doPointLayer(this);
      });
      this.$hub.$on("tabsPane-click", val => {
        doPointLayer(this);
      });
      //  点击工地
      this.$hub.$on("menu-item-click", ({ obj }) => {
        const { type, geometry } = obj;
        type == "point"
          ? this.goPointLoaction(geometry)
          : this.goPolygonLocation(geometry);
        this.doFrame = true;
      });
    },
    /**
     * 初始化地图对象,带上底图
     * @param {Function} fn 回调函数
     */
    createMap(fn) {
      const that = this;
      return new Promise((resolve, reject) => {
        loadModules(
          [
            "esri/Map",
            "esri/views/MapView",
            "esri/widgets/Legend",
            "esri/layers/VectorTileLayer",
            "esri/layers/TileLayer"
          ],
          OPTION
        ).then(([Map, MapView, Legend, VectorTileLayer]) => {
          // map加载底图
          that.map = new Map({
            spatialReference
          });
          //设置地图容器
          that.view = new MapView({
            container: that.$props.id,
            spatialReference,
            map: that.map,
            zoom: 12,
            center: [120.98066617591361, 28.116856434417986]
          });
          const layer = new VectorTileLayer({
            id: "vectorLayer",
            url: IMAGELAYER
          });
          that.map.add(layer);
          that.legend = new Legend({
            view: that.view
          });
          that.view.on("click", evt => {
            fetchPoint(
              that.$parent.$refs.leftMenu.tabsMenuData,
              evt.mapPoint,
              that.view,
              obj => {
                this.$hub.$emit("menu-item-click", { obj });
              }
            );
          });
          that.view.on("mouse-wheel", evt => {});
          resolve(true);
        });
      });
    },
    goPointLoaction({ x, y }) {
      this.view.goTo({ center: [x, y] });
    },
    goPolygonLocation({ rings }) {
      let x_ = 0,
        y_ = 0;
      rings[0].map(item => {
        x_ += item[0];
        y_ += item[1];
      });
      this.view.goTo({
        center: [x_ / rings[0].length, y_ / rings[0].length]
      });
    }
  }
};
</script>
